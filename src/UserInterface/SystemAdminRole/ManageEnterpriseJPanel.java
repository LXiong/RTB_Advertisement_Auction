    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Network network =(Network)netWorkjComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType)enterprisejComboBox.getSelectedItem();
        
        if(network==null||type==null){
            JOptionPane.showMessageDialog(null, "Invalid, you should select a network and enterprise type first.");
            return;
        }
        
        String name = nameJTextField.getText();
        
        if(Validation.stringIsEmpty(name)){
            JOptionPane.showMessageDialog(null, "Name is necessary and could contain just space, please check again.");
            nameJTextField.setText("");
        }
        
        boolean exist = false;
        for(Enterprise e : network.getEnterpriseDirecotory().getEnterpriseList()){
            if(e.getName().equals(name)){
                exist = true;
                break;
            }
        }
        
        if(!exist){
            alert.setText("");
            Enterprise enterprise = network.getEnterpriseDirecotory().createAndAddEnterprise(name, type);
            populateEnterpriseJTable();
        }
        else{
            alert.setText("Enteprise with same has existed in this network.");
            nameJTextField.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

