    private void populateEnterpriseJTable(){
        DefaultTableModel dtm = (DefaultTableModel)enterprisejTable1.getModel();
        dtm.setRowCount(0);
        
        for(Network network: system.getNetworkList()){
            for(Enterprise enterprise: network.getEnterpriseDirecotory().getEnterpriseList()){
                Object[] row = new Object[3];
                row[0] = enterprise;
                row[1] = network;
                row[2] = enterprise.getEnterpriseType().getValue();
                
                dtm.addRow(row);
            }
        }
    }
    
    private void populateComboBox(){
        netWorkjComboBox.removeAllItems();
        enterprisejComboBox.removeAllItems();
        
        for(Network network: system.getNetworkList()){
            netWorkjComboBox.addItem(network);
        }
        for(Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()){
            enterprisejComboBox.addItem(type);
        }
    }

