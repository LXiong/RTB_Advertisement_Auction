    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterprisejTable1.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please selected an Enterprise from table first.");
            return;
        }

        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to delete the Enterprise", "Warning",JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            Enterprise e = (Enterprise) enterprisejTable1.getValueAt(selectedRow, 0);
            Network n = (Network)enterprisejTable1.getValueAt(selectedRow, 1);
            n.getEnterpriseDirecotory().getEnterpriseList().remove(e);
            populateEnterpriseJTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


