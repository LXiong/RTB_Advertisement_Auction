    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)DSPCoopList.getModel();
        dtm.setRowCount(0);
        if(organization.getWorkQueue().getWorkRequestList()==null){
            organization.getWorkQueue().setWorkRequestList(new ArrayList());
        }
        for(WorkRequest dspwq : organization.getWorkQueue().getWorkRequestList()){
            if(dspwq instanceof DSPCoopWorkRequest){
                Object[] row = new Object[1];
                row[0] = (DSPCoopWorkRequest)dspwq;
                dtm.addRow(row);
            }
        }
    }
