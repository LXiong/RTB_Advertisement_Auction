    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = DSPCoopList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a DSP from the list first.");
            return;
        }
        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to approve the request?", "Warning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            DSPCoopWorkRequest d = (DSPCoopWorkRequest)DSPCoopList.getValueAt(selectedRow, 0);
            d.setReceiver(userAccount);
            d.getDsp().addCoopSSP(enterprise);
            organization.getWorkQueue().getWorkRequestList().remove(d);
            d.setStatus("Approved");
            enterprise.getCoopDSPList().add(d.getDsp());
            alert.setText("The Cooperation has been approved.");
            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


