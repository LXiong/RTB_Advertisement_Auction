    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)spaceList.getModel();
        dtm.setRowCount(0);
        
        if(organization.getWorkQueue().getWorkRequestList()==null){
            organization.getWorkQueue().setWorkRequestList(new ArrayList());
        }
        for(WorkRequest wq : organization.getWorkQueue().getWorkRequestList()){
            if(wq instanceof AssignSpaceWorkRequest){
                Object[] row = new Object[2];
                row[0] = (AssignSpaceWorkRequest)wq;
                row[1] = ((AssignSpaceWorkRequest)wq).getSpaceOwner();
                dtm.addRow(row);
            }
        }
    }
