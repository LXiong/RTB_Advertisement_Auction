    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = spaceList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a space from the list first.");
            return;
        }

        AssignSpaceWorkRequest asswq = (AssignSpaceWorkRequest)spaceList.getValueAt(selectedRow, 0);
        Space s = asswq.getSpace();

        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to assign this space?", "Warnning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            enterprise.getSpaceList().getSpaceList().add(s);
            asswq.setReceiver(userAccount);
            asswq.setStatus("Assigned");
            s.setStatus("Assigned");
            s.setSaler(enterprise);
            organization.getWorkQueue().getWorkRequestList().remove(asswq);
            SpacePayWorkRequest spwr = new SpacePayWorkRequest(s);
            spwr.setSender(userAccount);
            spwr.setPayEn(enterprise);
            spwr.setStatus("Assigned");
            userAccount.getWorkQueue().getWorkRequestList().add(spwr);
            organization.getWorkQueue().getWorkRequestList().add(spwr);
            for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
                if(o instanceof SSPFinancialOrganization){
                    o.getWorkQueue().getWorkRequestList().add(spwr);
                }
            }
            alert.setText("Space Approved Sucessfully!");
            populateTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


