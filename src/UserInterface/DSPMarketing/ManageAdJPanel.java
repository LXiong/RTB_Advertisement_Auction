    private void populateTable(){
        
            DefaultTableModel dtm = (DefaultTableModel)assignedAdList.getModel();
            dtm.setRowCount(0);
            
            if(enterprise.getAssignedAdsList().getAssignedAdList()==null){
                enterprise.getAssignedAdsList().setAssignedAdList(new ArrayList());
            }
            for(AssignedAd a : enterprise.getAssignedAdsList().getAssignedAdList()){
                Object[] row = new Object[2];
                row[0] = a;
                row[1] = a.getAssignedTime();
                dtm.addRow(row);
            
        }
    }
