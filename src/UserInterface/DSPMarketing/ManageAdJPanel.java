    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = assignedAdList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select and advertisement from the list first.");
            return;
        }
        
        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to withdraw the advertisement?", "Warning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            AssignedAd a = (AssignedAd) assignedAdList.getValueAt(selectedRow,0);
            enterprise.getAssignedAdsList().withdrawAd(a);
            a.getAd().setStatus("Withdrawed");
            for(WorkRequest wr : organization.getWorkQueue().getWorkRequestList()){
                if(wr instanceof AssignAdWorkRequest){
                    if(((AssignAdWorkRequest)wr).getOrgad().getAd().equals(a.getAd())){
                        ((AssignAdWorkRequest)wr).setStatus("Withdraw");
                    }
                }
            }
            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


