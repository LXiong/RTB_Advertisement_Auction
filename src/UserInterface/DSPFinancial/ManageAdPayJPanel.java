    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)adpay.getModel();
        dtm.setRowCount(0);
        
        if(organization.getWorkQueue().getWorkRequestList()==null){
            organization.getWorkQueue().setWorkRequestList(new ArrayList());
        }
        for(WorkRequest wr : organization.getWorkQueue().getWorkRequestList()){
            if(wr instanceof AdShowWorkRequest){
                Object[] row = new Object[4];
                row[0] = (AdShowWorkRequest)wr;
                row[1] = ((AdShowWorkRequest)wr).getShowAd().getSumbid();
                row[2] = ((AdShowWorkRequest)wr).getShowEn();
                row[3] = ((AdShowWorkRequest)wr).getShowAd().getAd().getAd().getOwner();
                dtm.addRow(row);
            }
        }
    }
