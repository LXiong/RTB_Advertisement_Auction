    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = adpay.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an Ad from the list first.");
            return;
        }
        
        //minus account of the enterprise
        AdShowWorkRequest adswr = (AdShowWorkRequest) adpay.getValueAt(selectedRow, 0);
        float payamount = adswr.getShowAd().getSumbid();
        enterprise.getFinancialAccount().makePayment(payamount);
        
        //send alert to SSP Financial dpt to receive the money
        AdPayWorkRequest adpwr = new AdPayWorkRequest(adswr.getShowAd());
        adpwr.setSender(userAccount);
        adpwr.setAdSender(enterprise);
        SSPEnterprise ssp = adswr.getShowEn();
        for(Organization ooo : ssp.getOrganizationDirectory().getOrganizationList()){
            if(ooo instanceof SSPFinancialOrganization){
                ooo.getWorkQueue().getWorkRequestList().add(adpwr);
                break;
            }
        }
        organization.getWorkQueue().getWorkRequestList().add(adpwr);
        userAccount.getWorkQueue().getWorkRequestList().add(adpwr);
        
        //send to advertiser to pay for the show
        ChargeAdvertiserWorkRequest cadwr = new ChargeAdvertiserWorkRequest();
        cadwr.setSaler(enterprise);
        cadwr.setShowAd(adswr.getShowAd());
        cadwr.setC(adswr.getC());
        cadwr.setSender(userAccount);
        cadwr.setShowPlace(adswr.getShowPlace());
       // if(adswr.getShowAd().getAd().getAd().getOwner().getOrganizationDirectory().getOrganizationList()==null){
         //   adswr.getShowAd().getAd().getAd().getOwner().getOrganizationDirectory().setOrganizationList(new ArrayList());
       // }
        for(Organization oooo : adswr.getShowAd().getAd().getAd().getOwner().getOrganizationDirectory().getOrganizationList()){
            if(oooo instanceof AdvertiserFinancialOrganization){
                oooo.getWorkQueue().getWorkRequestList().add(cadwr);
                break;
            }
        }
        organization.getWorkQueue().getWorkRequestList().add(cadwr);
        userAccount.getWorkQueue().getWorkRequestList().add(cadwr);
        alert.setText("Pay Request Send Sucessfully!");
        
        organization.getWorkQueue().getWorkRequestList().remove(adswr);
        populateTable();
        
    }//GEN-LAST:event_jButton1ActionPerformed


