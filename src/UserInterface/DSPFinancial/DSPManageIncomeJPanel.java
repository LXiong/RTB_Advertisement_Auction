    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = incomeList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the list first.");
            return;
        }

        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you have already received this payment?", "Warning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            

            ClickPayWorkRequest cpwr = (ClickPayWorkRequest) incomeList.getValueAt(selectedRow, 0);
            float in = cpwr.getShowAd().getSumbid();
            enterprise.getFinancialAccount().getIncome(in);
            
            cpwr.setReceiver(userAccount);
            
            AdChargeInvoice adcin= new AdChargeInvoice();
            adcin.setAd(cpwr.getShowAd().getAd().getAd());
            adcin.setSender(enterprise);
            adcin.setReceiver(cpwr.getPaySender());
            cpwr.getPaySender().getInvoiceList().getInvoiceList().add(adcin);
            
             organization.getWorkQueue().getWorkRequestList().remove(cpwr);
            alert.setText("Income Confirmed! Invoice Sent!");

            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


