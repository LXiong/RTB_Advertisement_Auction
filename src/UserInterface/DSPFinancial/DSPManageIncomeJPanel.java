    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)incomeList.getModel();
        dtm.setRowCount(0);
        
        if(organization.getWorkQueue().getWorkRequestList()==null){
            organization.getWorkQueue().setWorkRequestList(new ArrayList());
        }
        for(WorkRequest wr : organization.getWorkQueue().getWorkRequestList()){
            if(wr instanceof ClickPayWorkRequest){
                Object[] row = new Object[3];
                row[0] = (ClickPayWorkRequest)wr;
                row[1] = ((ClickPayWorkRequest)wr).getPaySender();
                row[2] = ((ClickPayWorkRequest)wr).getShowAd().getSumbid();
                dtm.addRow(row);
            }
        }
    }

