    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = spaceList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a space from the list first.");
            return;
        }

        Space s = (Space) spaceList.getValueAt(selectedRow, 1);
        
        String sta = (String)spaceList.getValueAt(selectedRow, 3);
        if(!sta.equals("unassigned")){
            JOptionPane.showMessageDialog(null, "The space you choose is not unassigned, please select another one.");
            return;
        }

        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to assign this space?", "Warnning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            AssignSpaceJPanel asspjp = new AssignSpaceJPanel(userProcessContainer, userAccount, s, organization ,enterprise, system);
            userProcessContainer.add("AssignSpaceJPanel", asspjp);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int selectedRow = spaceList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a space from table first.");
            return;
        }
        
        String sta = (String)spaceList.getValueAt(selectedRow, 3);
        if(!sta.equals("unassigned")){
            JOptionPane.showMessageDialog(null, "This space is under processing, you can't delete before check with SSP.");
            return;
        }

        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to delete the space?", "Warning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            Space s = (Space)spaceList.getValueAt(selectedRow, 1);
            enterprise.getSpaceList().getSpaceList().remove(s);
            populateTable();
        }
        else return;
    }//GEN-LAST:event_jButton4ActionPerformed


