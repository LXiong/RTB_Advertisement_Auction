    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        String name = nameJTextField.getText();
        String type = typeJTextField.getText();
        float price = Float.parseFloat(priceJTextField.getText());
        
        if(Validation.stringIsEmpty(name)||Validation.stringIsEmpty(type)||Validation.stringIsEmpty(priceJTextField.getText())){
            JOptionPane.showMessageDialog(null, "Make sure all the field have been filled.");
            return;
        }
        
        if(!Validation.allChar(name)){
            JOptionPane.showMessageDialog(null, "Make sure that name field is filled with all letters");
            nameJTextField.setText("");
            return;
        }
        
        if(!Validation.isFinBalance(priceJTextField.getText())){
            JOptionPane.showMessageDialog(null, "The price field should be filled with a validate float number.");
            priceJTextField.setText("");
            return;
        }
        
        boolean exist = false;
        for(Space s : enterprise.getSpaceList().getSpaceList()){
            if(s.getName().equals(name)){
                exist = true;
                break;
            }
        }

        if(!exist){
            alert.setText("");
            Space a = new Space();
            a.setName(name);
            a.setPageType(type);
            a.setPrice(price);  
            a.setOwner(enterprise);
            enterprise.getSpaceList().getSpaceList().add(a);
            populateTable();
        }
        else{
            alert.setText("Space with same name has already exist. Name should be unique, check again.");
            nameJTextField.setText("");
        }
    }//GEN-LAST:event_addJButtonActionPerformed

