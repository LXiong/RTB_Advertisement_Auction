    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)paymentList.getModel();
        dtm.setRowCount(0);
        if(organization.getWorkQueue().getWorkRequestList()== null){
            organization.getWorkQueue().setWorkRequestList(new ArrayList());
        }
        for(WorkRequest wq : organization.getWorkQueue().getWorkRequestList()){
            if(wq instanceof SpacePayWorkRequest){
                if(((SpacePayWorkRequest)wq).getStatus().equals("Assigned")){
                    Object[] row = new Object[3];
                    row[0] = (SpacePayWorkRequest)wq;
                    row[1] = ((SpacePayWorkRequest)wq).getSpace().getOwner();
                    row[2] = ((SpacePayWorkRequest)wq).getSpace().getPrice();
                    dtm.addRow(row);
                }
            }
        }
    }

