    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = paymentList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a space from the list first.");
            return;
        }
        
        SpacePayWorkRequest spwr = (SpacePayWorkRequest)paymentList.getValueAt(selectedRow, 0);
        
        int selectedValue = JOptionPane.showConfirmDialog(null, "You sure you want to make payment for this space?", "Warning", JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            spwr.setSender(userAccount);
            userAccount.getWorkQueue().getWorkRequestList().add(spwr);
            spwr.getSpace().setStatus("Paid");
            spwr.setStatus("Wait for confirm");
            for(Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
                if(o instanceof SSPMarketingOrganization){
                    o.getWorkQueue().getWorkRequestList().remove(spwr);
                    break;
                }
            }
            for(Organization oo : spwr.getSpace().getOwner().getOrganizationDirectory().getOrganizationList()){
                if(oo instanceof PublisherFinancialOrganization){
                    oo.getWorkQueue().getWorkRequestList().add(spwr);
                }
            }
            float minamount = spwr.getSpace().getPrice();
            if(enterprise.getFinancialAccount()==null){
                enterprise.setFinancialAccount(new FinancialAccount());
            }
            enterprise.getFinancialAccount().makePayment(minamount);
            alert.setText("Payment Made Sucessfully!");
            populateTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

