    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = spaceList.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a space from table first.");
            return;
        }
        
        String status = (String)spaceList.getValueAt(selectedRow, 3);
        if(status.equals("Paid") || status.equals("Confirm Receive")){
            JOptionPane.showMessageDialog(null, "The Space has already been paid, select another space.");
            return;
        }
        if(status.equals("Waiting Assignment")){
            JOptionPane.showMessageDialog(null, "Still waiting for approve, please wait.");
            return;
        }
        
        int selectedValue = JOptionPane.showConfirmDialog(null, "Are you sure you want to send a payment alert?", "Warning",JOptionPane.YES_NO_OPTION);
        if(selectedValue == JOptionPane.YES_OPTION){
            Space s = (Space) spaceList.getValueAt(selectedRow, 0);
            for(WorkRequest wq : organization.getWorkQueue().getWorkRequestList()){
                if(wq instanceof SpacePayAlertWorkRequest){
                    if(((SpacePayAlertWorkRequest)wq).getSpace().equals(s)){
                        JOptionPane.showMessageDialog(null, "This Space has already sent alert before, choose another one.");
                        return;
                    }
                }
            }
            SpacePayAlertWorkRequest spawq = new SpacePayAlertWorkRequest(s);
            SSPEnterprise ss = (SSPEnterprise)spaceList.getValueAt(selectedRow, 1);
            spawq.setSender(userAccount);
            spawq.setSpaceOwner(enterprise);
            spawq.setSpaceSaler(ss);
            userAccount.getWorkQueue().getWorkRequestList().add(spawq);
            organization.getWorkQueue().getWorkRequestList().add(spawq);
            for(Organization o : ss.getOrganizationDirectory().getOrganizationList()){
                if(o instanceof SSPFinancialOrganization){
                    ((SSPFinancialOrganization)o).getWorkQueue().getWorkRequestList().add(spawq);
                }
            }
            alert.setText("Pay Alert Send Sucessfully!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


