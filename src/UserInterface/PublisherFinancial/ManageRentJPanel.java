    private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)spaceList.getModel();
        dtm.setRowCount(0);
        if(enterprise.getSpaceList().getSpaceList() == null){
            enterprise.getSpaceList().setSpaceList(new ArrayList());
        }
        for(Space s : enterprise.getSpaceList().getSpaceList()){
            Object[] row = new Object[4];
            row[0] = s;
            row[1] = s.getSaler();
            row[2] = s.getPrice();
            row[3] = s.getStatus();
            dtm.addRow(row);
        }
    }
